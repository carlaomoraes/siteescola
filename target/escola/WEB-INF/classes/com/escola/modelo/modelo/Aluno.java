package modelo;

public class Aluno {
	int codAluno;
	String nomeAluno;
	public final int getCodAluno() {
		return codAluno;
	}
	public final void setCodAluno(int codAluno) {
		this.codAluno = codAluno;
	}
	public final String getNomeAluno() {
		return nomeAluno;
	}
	public final void setNomeAluno(String nomeAluno) {
		this.nomeAluno = nomeAluno;
	}
}
